package c.city.desolate.client.xml.bean;


public class C2SProtocolXML {
	private C2SHeadXML headXML;
	private C2SGameXML gameXML;
	private C2SMethodXML methodXML;

	public C2SHeadXML getHeadXML() {
		return headXML;
	}

	public void setHeadXML(C2SHeadXML headXML) {
		this.headXML = headXML;
	}

	public C2SGameXML getGameXML() {
		return gameXML;
	}

	public void setGameXML(C2SGameXML gameXML) {
		this.gameXML = gameXML;
	}

	public C2SMethodXML getMethodXML() {
		return methodXML;
	}

	public void setMethodXML(C2SMethodXML methodXML) {
		this.methodXML = methodXML;
	}

}
